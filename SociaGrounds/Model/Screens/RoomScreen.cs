        private readonly Map _map;
        private readonly Camera _camera;

        public bool IsPlayingMusic { get; set; }

        public RoomScreen()
        {
            Vector2 margin = new Vector2(60,36);

            _map = new Map(CreateMap((int)margin.X, (int)margin.Y), new Vector2(0, 0), margin);

            _map.AddSolidAsset(new Tree("Tree3", new Vector2(200, 200), 0, 1f));
            _map.AddSolidAsset(new Tree("Tree1", new Vector2(350, 300), 1, 1f));
            _map.AddSolidAsset(new Tree("Tree2", new Vector2(500, 250), 2, 1f));
            
            _camera = new Camera();

            IsPlayingMusic = false;
        }

        public void Update(GameTime gameTime, GraphicsDevice graphics)
        {
            if (!IsPlayingMusic)
            {
                SongPlayer.PlaySong(1, true);
                IsPlayingMusic = true;
            }

            KeyboardState oldstate =  SociaKeyBoard.CheckKeyState(gameTime);
            StaticPlayer.MyPlayer.Update(gameTime, oldstate);

            _camera.Update(graphics.Viewport, StaticPlayer.MyPlayer.Position, _map);

            StaticPlayer.UpdateForeignPlayers(gameTime);
        }

        public void Draw(SpriteBatch spriteBatch)
        {
            spriteBatch.Begin(SpriteSortMode.Deferred, BlendState.AlphaBlend, null, null, null, null, _camera.TranformPublic);

            _map.DrawNonSolid(spriteBatch);
            _map.DrawShade(spriteBatch);

            StaticPlayer.DrawPlayers(spriteBatch);

            _map.DrawSolid(spriteBatch);

            StaticPlayer.DrawPlayerText(spriteBatch);
            
            spriteBatch.End();
        }

        private int[,] CreateMap(int width, int height)
        {
            int[,] mapArray = new int[height,width];
            Random rand = new Random();

            for (int i = 0; i < height; i++)
            {
                for (int j = 0; j < width; j++)
                {
                    int number = rand.Next(0, 100);

                    if (number > 50)
                    {
                        mapArray[i, j] = 0;
                    }
                    else if (number > 70)
                    {
                        mapArray[i, j] = 1;
                    }
                    else 
                    {
                        mapArray[i, j] = rand.Next(2,8);
                    }
                }
            }
            return mapArray;
        }

