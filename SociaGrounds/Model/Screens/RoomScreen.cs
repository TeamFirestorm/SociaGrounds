        public bool IsPlayingMusic { get; set; }

        public RoomScreen(ContentManager content)
        {
            _map = new Map(CreateMap(), new Vector2(0, 0), content);
            _map.AddSolidAsset(new Tree(new Vector2(350, 300), 1, content));
            _map.AddSolidAsset(new Tree(new Vector2(500, 250), 2, content));
            _map.AddSolidAsset(new Tree(new Vector2(200, 200), 0, content));

            _camera = new Camera();
            

            IsPlayingMusic = false;
        }

        public void Update(GameTime gameTime, GraphicsDevice graphics)
        {
            if (!IsPlayingMusic)
            {
                SongPlayer.PlaySong(1, true);
                IsPlayingMusic = true;
            }

            Static.Keyboard.CheckKeyState();
            Static.Players[0].Update(gameTime, _map, Static.Keyboard.OldKeyboardState);

            _camera.Update(graphics.Viewport, Static.Players[0].Position, _map);

            //TODO implement movement for foreign players
            //foreach (var player in Static.Players)
            //{
            //    player.Update(gameTime, _ui, graphics.Viewport, _map, keyState);
            //}

        }

        public void Draw(SpriteBatch spriteBatch)
        {
            spriteBatch.Begin(SpriteSortMode.Deferred, BlendState.AlphaBlend, null, null, null, null, _camera.TranformPublic);
            _map.Draw(spriteBatch);
            foreach (var player in Static.Players)
            {
                player.Draw(spriteBatch);
            }
            _map.DrawSolid(spriteBatch);
            foreach (var player in Static.Players)
            {
                player.DrawText(spriteBatch);
            }
            
            spriteBatch.End();
        }

